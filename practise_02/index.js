// Write a program that prompts the user to enter a number, 
// and then outputs a message saying "The number is positive" 
// if the number is greater than 0, 
// and "The number is negative" if it is less than 0.
var num= parseInt(prompt("Enter any Integer:"))
if (num > 0) {
    console.log("The number is positive");
}
 else {
    console.log("The number is negative");
}