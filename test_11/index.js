// 11. Write a ts program to input week number and print week day.
var num=parseInt(prompt("week number"))
if (num==1) {
    console.log("monday");
}
else if (num==2) {
    console.log("tuesday");
}
else if (num==3) {
    console.log("wednesday");
}
else if (num==4) {
    console.log("thursday");
}
else if (num==5) {
    console.log("friday");
}
else if (num==6) {
    console.log("saturday");
}
else if (num==7) {
    console.log("sunday");
}
else {
    console.log("nothing");
}